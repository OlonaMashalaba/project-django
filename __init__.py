def add_nums(num1, num2):
    """This method will be used to add two numbers

        :param int num1: The first number
        :param int num2: The second number

        :returns: The sum of two numbers

        :rtype: int
    """
    answer = num1 + num2
    return answer
"""
This module performs division operations.

Functions:
    divide(a, b): Returns the division of a by b.
"""

def divide(a, b):
    """Returns the division of a by b."""
    return a / b


"""
This module performs multiplication operations.

Functions:
    multiply(a, b): Returns the product of a and b.
"""

def multiply(a, b):
    """Returns the product of a and b."""
    return a * b

"""
This module performs subtraction operations.

Functions:
    subtract(a, b): Returns the result of subtracting b from a.
"""

def subtract(a, b):
    """Returns the result of subtracting b from a."""
    return a - b

